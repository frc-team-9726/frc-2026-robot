package frc.robot.generated;

import static edu.wpi.first.units.Units.*;

import com.ctre.phoenix6.CANBus;
import com.ctre.phoenix6.configs.*;
import com.ctre.phoenix6.hardware.*;
import com.ctre.phoenix6.signals.*;
import com.ctre.phoenix6.swerve.*;
import com.ctre.phoenix6.swerve.SwerveModuleConstants.*;

import edu.wpi.first.math.Matrix;
import edu.wpi.first.math.geometry.Rotation2d;
import edu.wpi.first.math.numbers.N1;
import edu.wpi.first.math.numbers.N3;
import edu.wpi.first.units.measure.*;

import frc.robot.subsystems.CommandSwerveDrivetrain;

/**
 * Robot-specific constants for the swerve drivetrain, generated by Tuner X.
 * Gains and physical measurements must be tuned to match your specific robot.
 * https://v6.docs.ctr-electronics.com/en/stable/docs/tuner/tuner-swerve/index.html
 */
public class TunerConstants {

    // -------------------------------------------------------------------------
    // PID / Feedforward Gains
    // -------------------------------------------------------------------------

    // Controls the angle of each module's steering wheel.
    // Tune KP first; KS/KV compensate for static and velocity-dependent friction.
    private static final Slot0Configs steerGains = new Slot0Configs()
        .withKP(10).withKI(0).withKD(0.00001)
        .withKS(0.1).withKV(0.050).withKA(0)
        .withStaticFeedforwardSign(StaticFeedforwardSignValue.UseClosedLoopSign);

    // Controls the speed of each module's drive wheel.
    private static final Slot0Configs driveGains = new Slot0Configs()
        .withKP(0.005).withKI(0).withKD(0.1)
        .withKS(0).withKV(0.0124);

    // -------------------------------------------------------------------------
    // Motor & Encoder Configuration
    // -------------------------------------------------------------------------

    private static final ClosedLoopOutputType kSteerClosedLoopOutput = ClosedLoopOutputType.Voltage;
    private static final ClosedLoopOutputType kDriveClosedLoopOutput = ClosedLoopOutputType.Voltage;

    private static final DriveMotorArrangement kDriveMotorType = DriveMotorArrangement.TalonFX_Integrated;
    private static final SteerMotorArrangement kSteerMotorType = SteerMotorArrangement.TalonFX_Integrated;

    // FusedCANcoder requires a Phoenix Pro license; falls back to RemoteCANcoder if unlicensed.
    private static final SteerFeedbackType kSteerFeedbackType = SteerFeedbackType.FusedCANcoder;

    // Stator current at which the wheels begin to slip — raise if modules feel sluggish,
    // lower if wheels slip during acceleration.
    private static final Current kSlipCurrent = Amps.of(120);

    // Steer motors don't need high torque, so a lower stator limit helps prevent brownouts.
    private static final TalonFXConfiguration driveInitialConfigs = new TalonFXConfiguration();
    private static final TalonFXConfiguration steerInitialConfigs = new TalonFXConfiguration()
        .withCurrentLimits(new CurrentLimitsConfigs()
            .withStatorCurrentLimit(Amps.of(60))
            .withStatorCurrentLimitEnable(true)
        );
    private static final CANcoderConfiguration encoderInitialConfigs = new CANcoderConfiguration();

    // The Pigeon 2 is mounted upside-down on this robot, so roll is offset 180°.
private static final Pigeon2Configuration pigeonConfigs = new Pigeon2Configuration()
    .withMountPose(new MountPoseConfigs()
        .withMountPoseRoll(0)
        .withMountPoseYaw(0) 
    );

    // -------------------------------------------------------------------------
    // Drivetrain Physical Constants
    // -------------------------------------------------------------------------

    // All swerve devices must be on the same CAN bus.
    public static final CANBus kCANBus = new CANBus("", "./logs/example.hoot");

    public static final int kPigeonId = 9;

    // Theoretical top speed at 12 V — used to normalize joystick input.
    public static final LinearVelocity kSpeedAt12Volts = MetersPerSecond.of(4);

    // How many drive motor rotations result from one full azimuth rotation (due to belt/gear coupling).
    private static final double kCoupleRatio = 5.4;

    private static final double kDriveGearRatio = 7.67;
    private static final double kSteerGearRatio = 12.1;
    private static final Distance kWheelRadius = Inches.of(2);

    private static final boolean kInvertLeftSide  = false;
    private static final boolean kInvertRightSide = true;

    // -------------------------------------------------------------------------
    // Simulation-Only Constants
    // -------------------------------------------------------------------------

    private static final MomentOfInertia kSteerInertia = KilogramSquareMeters.of(0.01);
    private static final MomentOfInertia kDriveInertia = KilogramSquareMeters.of(0.01);
    private static final Voltage kSteerFrictionVoltage = Volts.of(0.2);
    private static final Voltage kDriveFrictionVoltage = Volts.of(0.2);

    // -------------------------------------------------------------------------
    // Swerve Module Layout
    // -------------------------------------------------------------------------

    public static final SwerveDrivetrainConstants DrivetrainConstants = new SwerveDrivetrainConstants()
        .withCANBusName(kCANBus.getName())
        .withPigeon2Id(kPigeonId)
        .withPigeon2Configs(pigeonConfigs);

    private static final SwerveModuleConstantsFactory<TalonFXConfiguration, TalonFXConfiguration, CANcoderConfiguration> ConstantCreator =
        new SwerveModuleConstantsFactory<TalonFXConfiguration, TalonFXConfiguration, CANcoderConfiguration>()
            .withDriveMotorGearRatio(kDriveGearRatio)
            .withSteerMotorGearRatio(kSteerGearRatio)
            .withCouplingGearRatio(kCoupleRatio)
            .withWheelRadius(kWheelRadius)
            .withSteerMotorGains(steerGains)
            .withDriveMotorGains(driveGains)
            .withSteerMotorClosedLoopOutput(kSteerClosedLoopOutput)
            .withDriveMotorClosedLoopOutput(kDriveClosedLoopOutput)
            .withSlipCurrent(kSlipCurrent)
            .withSpeedAt12Volts(kSpeedAt12Volts)
            .withDriveMotorType(kDriveMotorType)
            .withSteerMotorType(kSteerMotorType)
            .withFeedbackSource(kSteerFeedbackType)
            .withDriveMotorInitialConfigs(driveInitialConfigs)
            .withSteerMotorInitialConfigs(steerInitialConfigs)
            .withEncoderInitialConfigs(encoderInitialConfigs)
            .withSteerInertia(kSteerInertia)
            .withDriveInertia(kDriveInertia)
            .withSteerFrictionVoltage(kSteerFrictionVoltage)
            .withDriveFrictionVoltage(kDriveFrictionVoltage);

    // Module positions are measured from the robot center.
    // Encoder offsets are set by Tuner X — do not change manually.

    // Front Left
    private static final int     kFrontLeftDriveMotorId      = 7;
    private static final int     kFrontLeftSteerMotorId      = 2;
    private static final int     kFrontLeftEncoderId         = 11;
    private static final Angle   kFrontLeftEncoderOffset     = Rotations.of(0.28662109375);
    private static final boolean kFrontLeftSteerMotorInverted = false;
    private static final boolean kFrontLeftEncoderInverted    = false;
    private static final Distance kFrontLeftXPos = Inches.of(6.75);
    private static final Distance kFrontLeftYPos = Inches.of(13.75);

    // Front Right
    private static final int     kFrontRightDriveMotorId      = 4;
    private static final int     kFrontRightSteerMotorId      = 6;
    private static final int     kFrontRightEncoderId         = 13;
    private static final Angle   kFrontRightEncoderOffset     = Rotations.of(0.20263671875);
    private static final boolean kFrontRightSteerMotorInverted = false;
    private static final boolean kFrontRightEncoderInverted    = false;
    private static final Distance kFrontRightXPos = Inches.of(6.75);
    private static final Distance kFrontRightYPos = Inches.of(-13.75);

    // Back Left
    private static final int     kBackLeftDriveMotorId      = 3;
    private static final int     kBackLeftSteerMotorId      = 5;
    private static final int     kBackLeftEncoderId         = 12;
    private static final Angle   kBackLeftEncoderOffset     = Rotations.of(0.21044921875);
    private static final boolean kBackLeftSteerMotorInverted = false;
    private static final boolean kBackLeftEncoderInverted    = false;
    private static final Distance kBackLeftXPos = Inches.of(-6.75);
    private static final Distance kBackLeftYPos = Inches.of(13.75);

    // Back Right
    private static final int     kBackRightDriveMotorId      = 8;
    private static final int     kBackRightSteerMotorId      = 1;
    private static final int     kBackRightEncoderId         = 10;
    private static final Angle   kBackRightEncoderOffset     = Rotations.of(-0.199462890625);
    private static final boolean kBackRightSteerMotorInverted = false;
    private static final boolean kBackRightEncoderInverted    = false;
    private static final Distance kBackRightXPos = Inches.of(-6.75);
    private static final Distance kBackRightYPos = Inches.of(-13.75);

    public static final SwerveModuleConstants<TalonFXConfiguration, TalonFXConfiguration, CANcoderConfiguration> FrontLeft =
        ConstantCreator.createModuleConstants(
            kFrontLeftSteerMotorId,  kFrontLeftDriveMotorId,  kFrontLeftEncoderId,  kFrontLeftEncoderOffset,
            kFrontLeftXPos,  kFrontLeftYPos,  kInvertLeftSide,  kFrontLeftSteerMotorInverted,  kFrontLeftEncoderInverted
        );
    public static final SwerveModuleConstants<TalonFXConfiguration, TalonFXConfiguration, CANcoderConfiguration> FrontRight =
        ConstantCreator.createModuleConstants(
            kFrontRightSteerMotorId, kFrontRightDriveMotorId, kFrontRightEncoderId, kFrontRightEncoderOffset,
            kFrontRightXPos, kFrontRightYPos, kInvertRightSide, kFrontRightSteerMotorInverted, kFrontRightEncoderInverted
        );
    public static final SwerveModuleConstants<TalonFXConfiguration, TalonFXConfiguration, CANcoderConfiguration> BackLeft =
        ConstantCreator.createModuleConstants(
            kBackLeftSteerMotorId,   kBackLeftDriveMotorId,   kBackLeftEncoderId,   kBackLeftEncoderOffset,
            kBackLeftXPos,   kBackLeftYPos,   kInvertLeftSide,  kBackLeftSteerMotorInverted,   kBackLeftEncoderInverted
        );
    public static final SwerveModuleConstants<TalonFXConfiguration, TalonFXConfiguration, CANcoderConfiguration> BackRight =
        ConstantCreator.createModuleConstants(
            kBackRightSteerMotorId,  kBackRightDriveMotorId,  kBackRightEncoderId,  kBackRightEncoderOffset,
            kBackRightXPos,  kBackRightYPos,  kInvertRightSide, kBackRightSteerMotorInverted,  kBackRightEncoderInverted
        );

    // -------------------------------------------------------------------------
    // Factory Method
    // -------------------------------------------------------------------------

    /** Creates the drivetrain subsystem. Call this exactly once in RobotContainer. */
    public static CommandSwerveDrivetrain createDrivetrain(SwerveRequest.FieldCentric driveCentric) {
        return new CommandSwerveDrivetrain(
            driveCentric, DrivetrainConstants, FrontLeft, FrontRight, BackLeft, BackRight
        );
    }

    // -------------------------------------------------------------------------
    // CTRE SwerveDrivetrain Wrapper
    // -------------------------------------------------------------------------

    /** Typed SwerveDrivetrain using TalonFX drive/steer motors and CANcoders. */
    public static class TunerSwerveDrivetrain extends SwerveDrivetrain<TalonFX, TalonFX, CANcoder> {

        public TunerSwerveDrivetrain(
            SwerveDrivetrainConstants drivetrainConstants,
            SwerveModuleConstants<?, ?, ?>... modules
        ) {
            super(TalonFX::new, TalonFX::new, CANcoder::new, drivetrainConstants, modules);
        }

        public TunerSwerveDrivetrain(
            SwerveDrivetrainConstants drivetrainConstants,
            double odometryUpdateFrequency,
            SwerveModuleConstants<?, ?, ?>... modules
        ) {
            super(TalonFX::new, TalonFX::new, CANcoder::new, drivetrainConstants, odometryUpdateFrequency, modules);
        }

        /**
         * @param odometryStandardDeviation [x, y, theta]ᵀ in meters and radians
         * @param visionStandardDeviation   [x, y, theta]ᵀ in meters and radians
         */
        public TunerSwerveDrivetrain(
            SwerveDrivetrainConstants drivetrainConstants,
            double odometryUpdateFrequency,
            Matrix<N3, N1> odometryStandardDeviation,
            Matrix<N3, N1> visionStandardDeviation,
            SwerveModuleConstants<?, ?, ?>... modules
        ) {
            super(TalonFX::new, TalonFX::new, CANcoder::new, drivetrainConstants, odometryUpdateFrequency,
                  odometryStandardDeviation, visionStandardDeviation, modules);
        }
    }
}